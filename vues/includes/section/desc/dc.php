<p>
    La formation DIGITAL CODEUR a reçu le label "Grand Ecole du Numérique" qui est un réseau nationale de plus de 750 formations aux métiers du 
    numérique. La Grande Ecole du Numérique a pour ambition:
    <ul>
        <li>De répondre aux besoins en compétences numériques requis pour la transition numérique qui impacte tous les secteurs économiques.</li>
        <li>De Favoriser l'insertion sociale et professionelle des personnes éloignées de l'emploi, en particulier les personnes peu ou pas qualifiées.</li>
    </ul>
</p>

<p>  
    La formation Digital Codeur fait partie des "Formations pré-qualifiantes de remise à niveau numérique" permettant une montée de compétences pour poursuivre un parcours 
    certifiant ou trouver un emploi.
</p>

<p>
    L'objectif est de développer un socle de compétences numériques axées sur:
</p>   

<p>-La programmation,</p>
<p>-Le développement en environnement 2.0,</p>
<p>-Les bases du développement informatique.</p>