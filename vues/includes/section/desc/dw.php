<p>
    À partir d’un cahier des charges, d’instructions reçues d’un responsable de projet ou directement d’un client, le Designer Web analyse la demande, conçoit l’interface, 
    élabore une ambiance graphique, crée ou adapte une charte graphique et des médias. Il (elle) réalise des visuels ou des maquettes et élabore des animations.
</p>

<p>
    Pour la réalisation des outils de communication numériques tels que sites Web, applications Web pour mobiles, il (elle) intègre les médias en utilisant des logiciels professionnels, des frameworks, des langages
    de description ou de programmation. Il (elle) met en oeuvre et personnalise des systèmes de gestion de contenus (CMS).
</p>