<p>
    Le technicien supérieur systèmes et réseaux participe à la mise en service et au maintien en condition opérationnelle de l’infrastructure informatique. 
    Il intervient sur les systèmes et les réseaux, sur les éléments matériels et logiciels qui composent l’infrastructure, afin d’offrir aux utilisateurs et aux clients le niveau de 
    service attendu par l’entreprise.
</p>

<p>
    Le technicien supérieur systèmes et réseaux assiste les utilisateurs dans l’utilisation de leur équipement numérique.
    Il contribue au développement des compétences des utilisateurs en expliquant les règles de sécurité et de bon usage des logiciels et des outils collaboratifs en ligne et apporte 
    l’aide nécessaire à leur synchronisation.
</p>